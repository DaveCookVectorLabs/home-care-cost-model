def generate_dataset():
    fieldnames = [
        "id", "jurisdiction_code", "program_name", "administering_body",
        "eligibility_summary", "eligibility_assessment_tool",
        "typical_psw_hours_per_week_moderate", "typical_psw_hours_per_week_high",
        "typical_wait_days_initial", "typical_wait_days_ongoing",
        "user_fee_model", "scope_includes", "scope_excludes",
        "url", "source_id", "source_retrieved_date", "notes",
    ]
    rows = []
    for i, p in enumerate(PROGRAMS, start=1):
        row = {"id": i, **p}
        rows.append(row)

    with open(OUTPUT_FILE, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"Generated {len(rows)} rows -> {OUTPUT_FILE}")
    return len(rows)


if __name__ == "__main__":
    generate_dataset()
